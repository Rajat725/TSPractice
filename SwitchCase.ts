//Switch case example
let dayOfWeek:string="Thursday";
switch (dayOfWeek.toLowerCase()) {
    case "monday":
        console.log("Monday Here Baby");
        break;
        case "tuesday":
            console.log("Tuesday Here Baby");
            break;

            case "wednesday":
                console.log("Wednesday Here Baby");
                break;

                case "thursday":
                    console.log("Thursday Here Baby");
                    break;
    default:
        console.log("Wrong data here");
        break;
}